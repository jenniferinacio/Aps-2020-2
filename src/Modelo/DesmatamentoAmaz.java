
package Modelo;


public class DesmatamentoAmaz {
    private Integer id;
    private Integer ano;
    private Integer areaDesmatada;
    private String Estado;

    public Integer getAno() {
        return ano;
    }

    public void setAno(Integer ano) {
        this.ano = ano;
    }

    public Integer getAreaDesmatada() {
        return areaDesmatada;
    }

    public void setAreaDesmatada(Integer areaDesmatada) {
        this.areaDesmatada = areaDesmatada;
    }

   
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    

    public String getEstado() {
        return Estado;
    }

    public void setEstado(String Estado) {
        this.Estado = Estado;
    }
    
   
}
